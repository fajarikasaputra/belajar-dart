void main() {
  //Aritmetic Operators

  var a = 4;
  var b = 10;
  var c = a + b;

  //Operans = representasi dari data
  //Operators = operators pada operans

  print('Arithmetic Operators');
  //Penjumlahan
  var penjumlahan = a + b;

  //Pengurangan
  var pengurangan = a - b;

  //Perkalian
  var perkalian = a * b;

  //Pembagian
  var pembagian = a / b;

  //Modulo
  var mod = a % b;

  print(penjumlahan);
  print(pengurangan);
  print(pembagian);
  print(perkalian);
  print(mod);

  print('Equality & Relational Operators');
  //Equality & Realtional Operators
  print(a > b);
  print(a < b);
  print(a = b);
  print(a != b);
  print(a >= b);
  print(a <= b);

  print('Logical Operators');
  //Logical Operators
  bool x = true;
  bool y = false;

  // AND
  print(x && y);
  //OR
  print(x || y);
  //NOT
  print(!x);
  print(!y);
}
